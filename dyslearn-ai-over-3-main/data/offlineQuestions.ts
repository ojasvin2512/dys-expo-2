// Track used questions per session to avoid repeats
const usedIndices: Record<string, Set<number>> = {};

function getRandomUnused<T>(arr: T[], key: string, count: number): T[] {
  if (!usedIndices[key]) usedIndices[key] = new Set();
  if (usedIndices[key].size >= arr.length - count) {
    usedIndices[key] = new Set();
  }
  const available = arr.map((_, i) => i).filter(i => !usedIndices[key].has(i));
  const shuffled = available.sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, count);
  selected.forEach(i => usedIndices[key].add(i));
  return selected.map(i => arr[i]);
}

export function getRandomQuestions(challengeId: string, count = 5): any[] {
  switch (challengeId) {
    case 'scramble-1': return getRandomUnused(OFFLINE_SENTENCE_SCRAMBLE, 'scramble', count);
    case 'math-1': return getRandomUnused(OFFLINE_MATH_PROBLEMS, 'math', count);
    case 'pattern-1': return getRandomUnused(OFFLINE_PATTERNS, 'pattern', count);
    case 'synonym-1': return getRandomUnused(OFFLINE_SYNONYMS, 'synonym', count);
    case 'rhyme-1': return getRandomUnused(OFFLINE_RHYMES, 'rhyme', count);
    case 'odd-one-out-1': return getRandomUnused(OFFLINE_ODD_ONE_OUT, 'odd', count);
    case 'spelling-1': return getRandomUnused(OFFLINE_SPELLING, 'spelling', count);
    case 'story-1': return getRandomUnused(OFFLINE_STORY_STARTERS, 'story', 1);
    default: return [];
  }
}

